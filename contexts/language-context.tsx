function explicitTranslations(language: Language) {
  const entries = Object.entries(translations[language] ?? {})
  if (language === "de") {
    return Object.fromEntries(entries)
  }

  return Object.fromEntries(entries.filter(([key, value]) => defaultTranslations[key] !== value))
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>("de")
  const currentLanguage = languageOptions.find((option) => option.code === language) ?? languageOptions[1]
  const englishTranslations = explicitTranslations("en")
  const selectedTranslations =
    language === "de" ? defaultTranslations : language === "de-CH" ? explicitTranslations("de-CH") : explicitTranslations(language)
  const fallbackTranslations = language === "de" || language === "de-CH" ? defaultTranslations : englishTranslations
  const dictionary = {
    ...fallbackTranslations,
    ...selectedTranslations,
    ...(supplementalTranslations[language] ?? {}),
    ...(expandedHomepageTranslations[language] ?? {}),
    ...(detailPageTranslations.en ?? {}),
    ...(detailPageTranslations[language] ?? {}),
    ...(contactPageTranslations.en ?? {}),
    ...(contactPageTranslations[language] ?? {}),
    ...(pricingPageTranslations.en ?? {}),
    ...(pricingPageTranslations[language] ?? {}),
  }

  useEffect(() => {
    const savedLanguage = window.localStorage.getItem("letaiwork4u-language") as Language | null
    if (savedLanguage && languageOptions.some((option) => option.code === savedLanguage)) {
      setLanguage(savedLanguage)
    }
  }, [])

  useEffect(() => {
    window.localStorage.setItem("letaiwork4u-language", language)
    document.documentElement.lang = language
    document.documentElement.dir = currentLanguage.dir
  }, [currentLanguage.dir, language])

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      currentLanguage,
      isRTL: currentLanguage.dir === "rtl",
      t: (key: string) => dictionary[key] ?? (language === "de" || language === "de-CH" ? defaultTranslations[key] : undefined) ?? key,
    }),
    [currentLanguage, dictionary, language],
  )

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider")
  }
  return context
}
